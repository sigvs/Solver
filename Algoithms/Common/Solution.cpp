#include "Solution.h"

//************************************************************************************************
 Solution::Solution()
 {
     tTime=0;
 };
//************************************************************************************************
 Solution::Solution(const Solution& x)
 {
     tTime=0;
     *this=x;
 };
//************************************************************************************************
 void Solution::operator = (const Solution& x)
 {
     tTime=x.tTime;
 };
//************************************************************************************************
 void Solution::__FreeMemory()
 {
     tTime=0;
 };
//************************************************************************************************
 float Solution::fSecond()
 {
     return 1.0*tTime/CLOCKS_PER_SEC;
 };
//************************************************************************************************
 float Solution::fMinute()
 {
     return 1.0*tTime/(60*CLOCKS_PER_SEC);
 };
//************************************************************************************************
 float Solution::fHour()
 {
     return 1.0*tTime/(3600.0*CLOCKS_PER_SEC);
 };
//************************************************************************************************
 int Solution::iSecond()
 {
     return (int)fSecond();
 };
//************************************************************************************************
 int Solution::iMinute()
 {
     return (int)fMinute();
 };
//************************************************************************************************
 int Solution::iHour()
 {
     return (int)fHour();
 };
//************************************************************************************************
 Solution::~Solution()
 {
     __FreeMemory();
 };
//************************************************************************************************
 